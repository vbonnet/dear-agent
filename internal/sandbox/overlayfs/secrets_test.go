// TestSecretsNotInLowerdirAfterDestroy verifies cleanup doesn't leak secrets.
func TestSecretsNotInLowerdirAfterDestroy(t *testing.T) {
	if !isLinux() {
		t.Skip("OverlayFS tests only run on Linux")
	}

	if !hasKernel511() {
		t.Skip("OverlayFS rootless requires kernel 5.11+")
	}

	provider := overlayfs.NewProvider()
	ctx := context.Background()

	lowerDir := t.TempDir()
	workspaceDir := t.TempDir()

	req := sandbox.SandboxRequest{
		SessionID:    "cleanup-test",
		LowerDirs:    []string{lowerDir},
		WorkspaceDir: workspaceDir,
		Secrets: map[string]string{
			"TOP_SECRET": "super-secret-value",
		},
	}

	sb, err := provider.Create(ctx, req)
	if err != nil {
		var sbErr *sandbox.Error
		if errors.As(err, &sbErr) && sbErr.Code == sandbox.ErrCodeMountFailed {
			t.Skipf("Mount failed: %v", err)
		}
		require.NoError(t, err)
	}
	require.NotNil(t, sb)

	// Verify .env exists in upperdir before destroy
	envPath := filepath.Join(sb.UpperPath, ".env")
	_, err = os.Stat(envPath)
	require.NoError(t, err, ".env should exist before destroy")

	// Destroy sandbox
	err = provider.Destroy(ctx, sb.ID)
	require.NoError(t, err)

	// Verify lowerdir has no .env file
	lowerEnvPath := filepath.Join(lowerDir, ".env")
	_, err = os.Stat(lowerEnvPath)
	assert.True(t, os.IsNotExist(err), ".env should never exist in lowerdir")

	// Verify workspace directory is cleaned up (or at least .env is gone)
	_, err = os.Stat(envPath)
	// After destroy, the file should be gone (entire workspace cleaned up)
	if err == nil {
		// If workspace still exists, verify .env is gone
		t.Logf("Workspace still exists after destroy, checking .env is removed")
		assert.True(t, os.IsNotExist(err), ".env should be removed during cleanup")
	}
}

