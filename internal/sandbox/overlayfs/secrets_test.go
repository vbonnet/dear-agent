// TestSecretsInjection verifies basic secrets injection functionality.
func TestSecretsInjection(t *testing.T) {
	if !isLinux() {
		t.Skip("OverlayFS tests only run on Linux")
	}

	if !hasKernel511() {
		t.Skip("OverlayFS rootless requires kernel 5.11+")
	}

	provider := overlayfs.NewProvider()
	ctx := context.Background()

	// Create test directories
	lowerDir := t.TempDir()
	workspaceDir := t.TempDir()

	// Set up environment variable for expansion test
	t.Setenv("TEST_HOME", "/test/home/path")

	req := sandbox.SandboxRequest{
		SessionID:    "secrets-test",
		LowerDirs:    []string{lowerDir},
		WorkspaceDir: workspaceDir,
		Secrets: map[string]string{
			"API_KEY":    "secret-value-123",
			"DATABASE":   "postgres://user:pass@localhost:5432/db",
			"EXPAND_VAR": "${TEST_HOME}/path",
		},
	}

	sb, err := provider.Create(ctx, req)
	if err != nil {
		var sbErr *sandbox.Error
		if errors.As(err, &sbErr) && sbErr.Code == sandbox.ErrCodeMountFailed {
			t.Skipf("Mount failed (may need permissions): %v", err)
		}
		require.NoError(t, err)
	}
	require.NotNil(t, sb)
	defer func() {
		_ = provider.Destroy(ctx, sb.ID)
	}()

	// Verify .env exists in upperdir
	envPath := filepath.Join(sb.UpperPath, ".env")
	content, err := os.ReadFile(envPath)
	require.NoError(t, err, ".env file should exist in upperdir")

	contentStr := string(content)

	// Verify header comments
	assert.Contains(t, contentStr, "# Auto-generated by AGM sandbox")
	assert.Contains(t, contentStr, "# DO NOT COMMIT THIS FILE")

	// Verify secrets are present
	assert.Contains(t, contentStr, "API_KEY=secret-value-123")
	assert.Contains(t, contentStr, "DATABASE=postgres://user:pass@localhost:5432/db")

	// Verify environment variable expansion happened
	assert.Contains(t, contentStr, "/test/home/path/path")
	assert.NotContains(t, contentStr, "${TEST_HOME}")

	// Verify file permissions (0600)
	stat, err := os.Stat(envPath)
	require.NoError(t, err)
	assert.Equal(t, os.FileMode(0600), stat.Mode().Perm(), "Secrets file should have 0600 permissions")

	// Verify .env NOT in lowerdir
	_, err = os.Stat(filepath.Join(lowerDir, ".env"))
	assert.True(t, os.IsNotExist(err), ".env should not exist in lowerdir")

	// Verify .env is visible in merged view
	mergedEnvPath := filepath.Join(sb.MergedPath, ".env")
	mergedContent, err := os.ReadFile(mergedEnvPath)
	require.NoError(t, err, ".env should be visible in merged view")
	assert.Equal(t, contentStr, string(mergedContent), "Merged .env should match upper .env")
}

// TestSecretsIsolation verifies secrets don't leak between sandboxes.
func TestSecretsIsolation(t *testing.T) {
	if !isLinux() {
		t.Skip("OverlayFS tests only run on Linux")
	}

	if !hasKernel511() {
		t.Skip("OverlayFS rootless requires kernel 5.11+")
	}

	provider := overlayfs.NewProvider()
	ctx := context.Background()

	// Create shared lowerdir
	lowerDir := t.TempDir()

	// Create first sandbox with secrets
	workspace1 := t.TempDir()
	req1 := sandbox.SandboxRequest{
		SessionID:    "sandbox-1",
		LowerDirs:    []string{lowerDir},
		WorkspaceDir: workspace1,
		Secrets: map[string]string{
			"SECRET_1": "value-from-sandbox-1",
			"SHARED":   "sandbox1-value",
		},
	}

	sb1, err := provider.Create(ctx, req1)
	if err != nil {
		var sbErr *sandbox.Error
		if errors.As(err, &sbErr) && sbErr.Code == sandbox.ErrCodeMountFailed {
			t.Skipf("Mount failed: %v", err)
		}
		require.NoError(t, err)
	}
	require.NotNil(t, sb1)
	defer func() {
		_ = provider.Destroy(ctx, sb1.ID)
	}()

	// Create second sandbox with different secrets
	workspace2 := t.TempDir()
	req2 := sandbox.SandboxRequest{
		SessionID:    "sandbox-2",
		LowerDirs:    []string{lowerDir},
		WorkspaceDir: workspace2,
		Secrets: map[string]string{
			"SECRET_2": "value-from-sandbox-2",
			"SHARED":   "sandbox2-value",
		},
	}

	sb2, err := provider.Create(ctx, req2)
	if err != nil {
		var sbErr *sandbox.Error
		if errors.As(err, &sbErr) && sbErr.Code == sandbox.ErrCodeMountFailed {
			t.Skipf("Mount failed: %v", err)
		}
		require.NoError(t, err)
	}
	require.NotNil(t, sb2)
	defer func() {
		_ = provider.Destroy(ctx, sb2.ID)
	}()

	// Verify sandbox 1 secrets
	env1Path := filepath.Join(sb1.UpperPath, ".env")
	content1, err := os.ReadFile(env1Path)
	require.NoError(t, err)
	content1Str := string(content1)

	assert.Contains(t, content1Str, "SECRET_1=value-from-sandbox-1")
	assert.Contains(t, content1Str, "SHARED=sandbox1-value")
	assert.NotContains(t, content1Str, "SECRET_2")
	assert.NotContains(t, content1Str, "sandbox2-value")

	// Verify sandbox 2 secrets
	env2Path := filepath.Join(sb2.UpperPath, ".env")
	content2, err := os.ReadFile(env2Path)
	require.NoError(t, err)
	content2Str := string(content2)

	assert.Contains(t, content2Str, "SECRET_2=value-from-sandbox-2")
	assert.Contains(t, content2Str, "SHARED=sandbox2-value")
	assert.NotContains(t, content2Str, "SECRET_1")
	assert.NotContains(t, content2Str, "sandbox1-value")

	// Verify lowerdir has no .env file
	_, err = os.Stat(filepath.Join(lowerDir, ".env"))
	assert.True(t, os.IsNotExist(err), "No .env should exist in shared lowerdir")
}

