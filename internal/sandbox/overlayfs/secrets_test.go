// TestSecretsEnvironmentExpansion tests various environment variable expansion scenarios.
func TestSecretsEnvironmentExpansion(t *testing.T) {
	if !isLinux() {
		t.Skip("OverlayFS tests only run on Linux")
	}

	if !hasKernel511() {
		t.Skip("OverlayFS rootless requires kernel 5.11+")
	}

	provider := overlayfs.NewProvider()
	ctx := context.Background()

	// Set up test environment variables
	t.Setenv("TEST_VAR", "test_value")
	t.Setenv("PATH_PREFIX", "/usr/local")
	t.Setenv("NESTED_VAR", "nested_${TEST_VAR}")

	lowerDir := t.TempDir()
	workspaceDir := t.TempDir()

	req := sandbox.SandboxRequest{
		SessionID:    "expansion-test",
		LowerDirs:    []string{lowerDir},
		WorkspaceDir: workspaceDir,
		Secrets: map[string]string{
			// Simple expansion
			"SIMPLE": "${TEST_VAR}",
			// Expansion in middle of string
			"PATH": "${PATH_PREFIX}/bin",
			// Multiple expansions
			"MULTI": "${TEST_VAR}_and_${PATH_PREFIX}",
			// No expansion needed
			"LITERAL": "no_variables_here",
			// Nested expansion (will expand NESTED_VAR but not nested TEST_VAR)
			"NESTED": "${NESTED_VAR}",
			// Non-existent variable (should remain as-is or expand to empty)
			"MISSING": "${NONEXISTENT_VAR}",
		},
	}

	sb, err := provider.Create(ctx, req)
	if err != nil {
		var sbErr *sandbox.Error
		if errors.As(err, &sbErr) && sbErr.Code == sandbox.ErrCodeMountFailed {
			t.Skipf("Mount failed: %v", err)
		}
		require.NoError(t, err)
	}
	require.NotNil(t, sb)
	defer func() {
		_ = provider.Destroy(ctx, sb.ID)
	}()

	envPath := filepath.Join(sb.UpperPath, ".env")
	content, err := os.ReadFile(envPath)
	require.NoError(t, err)
	contentStr := string(content)

	// Verify expansions
	assert.Contains(t, contentStr, "SIMPLE=test_value")
	assert.Contains(t, contentStr, "PATH=/usr/local/bin")
	assert.Contains(t, contentStr, "MULTI=test_value_and_/usr/local")
	assert.Contains(t, contentStr, "LITERAL=no_variables_here")
	assert.Contains(t, contentStr, "NESTED=nested_${TEST_VAR}") // Only first level expanded

	// Non-existent variables expand to empty string
	lines := strings.Split(contentStr, "\n")
	for _, line := range lines {
		if strings.HasPrefix(line, "MISSING=") {
			// Should not contain the ${...} syntax
			assert.NotContains(t, line, "${NONEXISTENT_VAR}")
		}
	}
}

