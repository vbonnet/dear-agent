// TestWriteSecretsUnit tests the writeSecrets function directly without mounting.
// This test can run on any platform without requiring OverlayFS support.
func TestWriteSecretsUnit(t *testing.T) {
	provider := NewProvider()
	upperDir := t.TempDir()

	// Set up test environment variable
	t.Setenv("TEST_VAR", "test_value")

	secrets := map[string]string{
		"API_KEY":    "secret-123",
		"DATABASE":   "postgres://localhost",
		"EXPAND_VAR": "${TEST_VAR}/expanded",
	}

	err := provider.writeSecrets(upperDir, secrets)
	require.NoError(t, err)

	// Verify .env file exists
	envPath := filepath.Join(upperDir, ".env")
	content, err := os.ReadFile(envPath)
	require.NoError(t, err)

	contentStr := string(content)

	// Verify header
	assert.Contains(t, contentStr, "# Auto-generated by AGM sandbox")
	assert.Contains(t, contentStr, "# DO NOT COMMIT THIS FILE")

	// Verify secrets
	assert.Contains(t, contentStr, "API_KEY=secret-123")
	assert.Contains(t, contentStr, "DATABASE=postgres://localhost")

	// Verify expansion
	assert.Contains(t, contentStr, "test_value/expanded")
	assert.NotContains(t, contentStr, "${TEST_VAR}")

	// Verify permissions
	stat, err := os.Stat(envPath)
	require.NoError(t, err)
	assert.Equal(t, os.FileMode(0600), stat.Mode().Perm())
}

