// TestWriteSecretsSpecialCharacters verifies special character handling.
func TestWriteSecretsSpecialCharacters(t *testing.T) {
	provider := NewProvider()
	upperDir := t.TempDir()

	secrets := map[string]string{
		"URL":      "https://user:p@ss@example.com:8080",
		"SPACES":   "value with spaces",
		"QUOTES":   `value with "quotes"`,
		"NEWLINES": "line1\nline2",
		"EQUALS":   "key=value=more",
	}

	err := provider.writeSecrets(upperDir, secrets)
	require.NoError(t, err)

	envPath := filepath.Join(upperDir, ".env")
	content, err := os.ReadFile(envPath)
	require.NoError(t, err)

	contentStr := string(content)

	// Verify all special characters are preserved
	assert.Contains(t, contentStr, "URL=https://user:p@ss@example.com:8080")
	assert.Contains(t, contentStr, "SPACES=value with spaces")
	assert.Contains(t, contentStr, `QUOTES=value with "quotes"`)
	assert.Contains(t, contentStr, "NEWLINES=line1\nline2")
	assert.Contains(t, contentStr, "EQUALS=key=value=more")
}

// TestWriteSecretsEnvironmentExpansion verifies variable expansion behavior.
func TestWriteSecretsEnvironmentExpansion(t *testing.T) {
	provider := NewProvider()
	upperDir := t.TempDir()

	// Set up multiple environment variables
	t.Setenv("VAR1", "value1")
	t.Setenv("VAR2", "value2")
	t.Setenv("PATH_PREFIX", "/usr/local")

	secrets := map[string]string{
		"SIMPLE":  "${VAR1}",
		"PATH":    "${PATH_PREFIX}/bin",
		"MULTI":   "${VAR1}_and_${VAR2}",
		"LITERAL": "no_expansion",
		"MISSING": "${NONEXISTENT}",
	}

	err := provider.writeSecrets(upperDir, secrets)
	require.NoError(t, err)

	envPath := filepath.Join(upperDir, ".env")
	content, err := os.ReadFile(envPath)
	require.NoError(t, err)

	contentStr := string(content)

	// Verify expansions
	assert.Contains(t, contentStr, "SIMPLE=value1")
	assert.Contains(t, contentStr, "PATH=/usr/local/bin")
	assert.Contains(t, contentStr, "MULTI=value1_and_value2")
	assert.Contains(t, contentStr, "LITERAL=no_expansion")

	// Missing variables expand to empty string
	lines := strings.Split(contentStr, "\n")
	for _, line := range lines {
		if strings.HasPrefix(line, "MISSING=") {
			// Should not contain ${...} syntax
			assert.NotContains(t, line, "${NONEXISTENT}")
		}
	}
}

// TestWriteSecretsPermissions verifies strict file permissions.
func TestWriteSecretsPermissions(t *testing.T) {
	provider := NewProvider()
	upperDir := t.TempDir()

	secrets := map[string]string{
		"SECRET": "value",
	}

	err := provider.writeSecrets(upperDir, secrets)
	require.NoError(t, err)

	envPath := filepath.Join(upperDir, ".env")
	stat, err := os.Stat(envPath)
	require.NoError(t, err)

	// Must be exactly 0600 (owner read/write only)
	perms := stat.Mode().Perm()
	assert.Equal(t, os.FileMode(0600), perms)

	// No group or other permissions
	assert.Equal(t, os.FileMode(0), perms&0077)
}
